/**
 * scripts/build-blog-data.mjs
 *
 * Runs as a prebuild/predev step, NEVER as part of the Next.js app bundle.
 *
 * Why this exists: lib/blog.ts used to call fs.readFileSync directly,
 * which risks a broken build on Cloudflare's Workers runtime (no fs
 * support, and OpenNext's bundler has a documented history of pulling
 * fs-touching code into the edge bundle even when it's logically
 * build-time-only). This script removes that risk entirely by doing
 * all file reading here, in plain Node, before Next.js ever runs.
 *
 * Output: lib/blog-data.generated.ts — a plain TS module with zero
 * runtime dependencies. Regenerated on every `npm run dev` and
 * `npm run build`, never hand-edited, safe to .gitignore.
 */

import fs from 'fs'
import path from 'path'
import matter from 'gray-matter'

const BLOG_DIR    = path.join(process.cwd(), 'content', 'blog')
const OUTPUT_FILE = path.join(process.cwd(), 'lib', 'blog-data.generated.ts')

/* ─── Sanitise frontmatter strings ───────────────────────────── */
// Strips smart quotes and em dashes that corrupt YAML parsing or
// violate the brand's no-em-dash rule.
const REPLACEMENTS = [
  [/\u2014/g, '-'],
  [/\u2013/g, '-'],
  [/\u2018/g, "'"],
  [/\u2019/g, "'"],
  [/\u201C/g, '"'],
  [/\u201D/g, '"'],
]

function sanitise(value) {
  if (typeof value !== 'string') return value
  let v = value
  for (const [regex, replacement] of REPLACEMENTS) {
    v = v.replace(regex, replacement)
  }
  return v
}

function sanitiseFrontmatter(data) {
  const out = {}
  for (const [key, value] of Object.entries(data)) {
    out[key] = sanitise(value)
  }
  return out
}

/* ─── Read and parse every post ──────────────────────────────── */
function buildPosts() {
  if (!fs.existsSync(BLOG_DIR)) {
    console.warn(`[build-blog-data] ${BLOG_DIR} does not exist, writing empty post list.`)
    return []
  }

  const files = fs
    .readdirSync(BLOG_DIR)
    .filter((f) => f.endsWith('.mdx') || f.endsWith('.md'))

  const posts = []

  for (const file of files) {
    const slug     = file.replace(/\.(mdx|md)$/, '')
    const filePath = path.join(BLOG_DIR, file)
    const raw      = fs.readFileSync(filePath, 'utf-8')
    const { data: rawData, content } = matter(raw)
    const data = sanitiseFrontmatter(rawData)

    if (!data.title || typeof data.title !== 'string') {
      console.warn(`[build-blog-data] Missing title, skipping: ${filePath}`)
      continue
    }

    posts.push({
      slug,
      title:       data.title       || '',
      description: data.description || '',
      date:        data.date        || '',
      keyword:     data.keyword     || '',
      category:    data.category    || 'techniques',
      readingTime: data.readingTime || 5,
      coliType:    data.coliType    || 'ALL',
      content:     sanitise(content),
    })
  }

  // Sort newest first, once, here, so consuming code doesn't need to.
  posts.sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())

  return posts
}

/* ─── Write the generated module ─────────────────────────────── */
function writeOutput(posts) {
  const banner =
    '/**\n' +
    ' * AUTO-GENERATED by scripts/build-blog-data.mjs\n' +
    ' * Do not edit by hand, your changes will be overwritten on the next build.\n' +
    ' * Edit content/blog/*.mdx instead.\n' +
    ' */\n\n'

  const body =
    `export const BLOG_POSTS = ${JSON.stringify(posts, null, 2)} as const\n`

  fs.writeFileSync(OUTPUT_FILE, banner + body, 'utf-8')
  console.log(`[build-blog-data] Wrote ${posts.length} post(s) to ${OUTPUT_FILE}`)
}

const posts = buildPosts()
writeOutput(posts)
